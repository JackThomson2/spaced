pub unsafe fn de_space_str(string: &mut str) -> usize {
    let mut i = 0;
    let mut pos = 0;
    let bytes = string.as_bytes_mut();

    let byte_len = bytes.len();

    while i < byte_len {
        let c = *bytes.get_unchecked(i);
        *bytes.get_unchecked_mut(pos) = c;

        pos += *JUMP_TABLE.get_unchecked(c as usize);
        i += 1;
    }

    pos
}
